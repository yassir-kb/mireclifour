package com.example.mireclifour.model;

import java.util.ArrayList;
import java.util.List;

public class User {
    private String id, firstname, lastname, mail, number, dob, gender, type, logo, password;
    List<Product> products = new ArrayList<>();


    public User(String firstname, String lastname, String mail, String number, String dob, String gender, String type, String logo, String password) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.mail = mail;
        this.number = number;
        this.dob = dob;
        this.gender = gender;
        this.type = type;
        this.logo = logo;
        this.password = password;
    }

    public User() {
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public List<Product> getProducts() {
        return products;
    }

    public void setProducts(List<Product> products) {
        this.products = products;
    }
}